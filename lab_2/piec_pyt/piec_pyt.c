#include <stdio.h>

int p = 0, q = 32, n = 0, anw, average;

int main() {
    printf("-----------------------------------------------------------\n");
    printf("Pomysl liczbe x z przedzialu 0 - 31, a ja zgadne ja w 5 krokach!\n");
    printf("-----------------------------------------------------------\n");

    for (int i = 1; i <= 5; i++) {
        average = (p + q) / 2;
        printf("Czy x >= %i? (1 - tak / 0 - nie): ", average);
        scanf("%i", &anw);
        if (anw == 1) 
		{
            p = average + 1;
        } 
		else 
		{
            q = average;
        }
		n = n * 2 + anw;
    }


    printf("Liczba jest rowna: %i\n", n);
    return 0;
}
